    private void Payment_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Payment_ButtonActionPerformed
        // TODO add your handling code here:
        String totalpay = totalpayText.getText();
        int n = Integer.parseInt(totalpay);
        String customerpay = customerPayText.getText();
        int n1 = Integer.parseInt(customerpay);
        String rn = Roomnumber_combo.getSelectedItem().toString();
        if (n == n1) {
            JOptionPane.showMessageDialog(null, "결제가 완료 되었습니다.");

            String roomch = roomchargeText.getText();
            String res = resText.getText();
            String sev = serviceText.getText();

            String tt = totalpayText.getText();

            try {
                File file = new File("Payment.txt");
                FileWriter writer = null;
                String s = "/";

                writer = new FileWriter(file, true);
                writer.write(roomch);
                writer.write(s);//방 가격 
                writer.write(res);
                writer.write(s); //레스토랑
                writer.write(sev);
                writer.write(s); //룸서비스금액
                writer.write(tt); //총금액
                writer.flush();// 출력은 버퍼에 쌓여있기에 쌓인 버퍼를 목적지로 보내줌
                writer.close();
            } catch (IOException e) {
                System.out.println("오류발생");
            }
        } else {
            JOptionPane.showMessageDialog(null, "결제해야할 금액과 지불할 금액이 다릅니다.");
        }

        FileWriter fw;
        try (FileReader fr = new FileReader("Reservation.txt")) {
            BufferedReader reader = new BufferedReader(fr);
            String line;
            String[] lineArr;
            String asd = "";
            while ((line = reader.readLine()) != null) {
                lineArr = line.split("/");
                if (!lineArr[9].equals(rn)) {
                    asd += line + "\r\n";
                }
            }
            fw = new FileWriter("Reservation.txt");
            fw.write(asd);
            fw.close();
            fr.close();
        } catch (Exception e) {
        }

    }//GEN-LAST:event_Payment_ButtonActionPerformed

