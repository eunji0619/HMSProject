    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String rn = Roomnumber_combo.getSelectedItem().toString();

        String billroomcharge = roomchargeText.getText();
        String billres = resText.getText();
        String billservice = serviceText.getText();

        try {
            try (FileReader fr = new FileReader("RoomPrice.txt")) {
                Scanner reader = new Scanner(fr);
                String line = null;
                String[] lineArr;

                while ((line = reader.nextLine()) != null) {
                    lineArr = line.split("/");

                    roomchargeText.setText(lineArr[1]);

                }
                fr.close();
            } catch (Exception e) {
            }

            try (FileReader fr = new FileReader("RestaurantService.txt")) {
                Scanner reader = new Scanner(fr);
                String line = null;
                String[] lineArr;
                while ((line = reader.nextLine()) != null) {
                    lineArr = line.split("/");
                    resText.setText(lineArr[8]);
                }
                fr.close();
            } catch (Exception e) {
            }

            try (FileReader fr = new FileReader("RoomService.txt")) {
                Scanner reader = new Scanner(fr);
                String line = null;
                String[] lineArr;

                while ((line = reader.nextLine()) != null) {
                    lineArr = line.split("/");
                    serviceText.setText(lineArr[8]);
                }
                fr.close();
            } catch (Exception e) {
            }

            int roomch = Integer.parseInt(billroomcharge);
            int res = Integer.parseInt(billres);
            int sev = Integer.parseInt(billservice);

            int total = 0;
            total = roomch + res + sev;
            totalpayText.setText("" + total);
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton2ActionPerformed

