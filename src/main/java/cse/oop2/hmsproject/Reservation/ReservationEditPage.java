            while ((s = bos.readLine()) != null) {
                array = s.split("/");
                if (str.equals(array[0]) || str.equals(array[9])) {
                    Customer_Edit.setText("" + array[0]);
                    CustomerPn_Edit.setText("" + array[1]);
                    CustomerAccount_Edit.setText("" + array[2]);
                    CheckinYear_Edit.setSelectedItem("" + array[3]);
                    CheckinMonth_Edit.setSelectedItem("" + array[4]);
                    CheckinDay_Edit.setSelectedItem("" + array[5]);
                    CheckoutYear_Edit.setSelectedItem("" + array[6]);
                    CheckoutMonth_Edit.setSelectedItem("" + array[7]);
                    CheckoutDay_Edit.setSelectedItem("" + array[8]);
                    Roomnumber_Edit.setSelectedItem("" + array[9]);
                }
            }
            bos.close();
