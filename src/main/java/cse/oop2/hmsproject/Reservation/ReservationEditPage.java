    private void Edit_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_ButtonActionPerformed
        // TODO add your handling code here:
        String name = Customer_Edit.getText();//예약자
        String ph = CustomerPn_Edit.getText();
        String account = CustomerAccount_Edit.getText();
        String CheckInYear = CheckinYear_Edit.getSelectedItem().toString();
        String CheckInMonth = CheckinMonth_Edit.getSelectedItem().toString();
        String CheckInDay = CheckinDay_Edit.getSelectedItem().toString();
        String CheckOutYear = CheckoutYear_Edit.getSelectedItem().toString();
        String CheckOutMonth = CheckoutMonth_Edit.getSelectedItem().toString();
        String CheckOutDay = CheckoutDay_Edit.getSelectedItem().toString();
        String Roomnum = Roomnumber_Edit.getSelectedItem().toString();

        ArrayList<String> tempArray = new ArrayList<>();

        try (FileReader fr = new FileReader("Reservation.txt")) {
            Scanner reader = new Scanner(fr);
            String line = null;
            String[] lineArr;

            while ((line = reader.nextLine()) != null) {
                lineArr = line.split("/");
                if (lineArr[0].equals(name)) {
                    tempArray.add(
                            lineArr[0] + "/"
                            + ph + "/"
                            + account + "/"
                            + CheckInYear + "/"
                            + CheckInMonth + "/"
                            + CheckInDay + "/"
                            + CheckOutYear + "/"
                            + CheckOutMonth + "/"
                            + CheckOutDay + "/"
                            + Roomnum);
                } else {
                    tempArray.add(line);
                }
            }
            fr.close();
        } catch (Exception e) {
        }

        try (PrintWriter pr = new PrintWriter("Reservation.txt")) {
            for (String str : tempArray) {
                pr.println(str);
            }
            pr.close();
        } catch (Exception e) {
        }

        JOptionPane.showMessageDialog(null, "수정이 완료 되었습니다.");
    }//GEN-LAST:event_Edit_ButtonActionPerformed

